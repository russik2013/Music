                                The Afghan Whigs' sound was growing larger by the release during the days on Sub
                                Pop, so
                                the
                                fact that Gentlemen turned out the way it did
                                wasn't all that surprising as a result ("cinematic" was certainly the word the band
                                was
                                aiming for, what with credits describing the recording
                                process as being "shot on location" at Ardent Studios). While Gentlemen is no
                                monolith,
                                it
                                is very much of a piece at the start. While "If I Were
                                Going" opens things on a slightly moodier tip, it's the crunch of "Gentlemen," "Be
                                Sweet,"
                                and "Debonair" that really stands out, each of which
                                features a tightly wound R&B punch that rocks out as much as it grooves, if not more
                                so.
                                Greg Dulli's lyrics immediately set about the task of
                                emotional self-evisceration at the same time, with lines like "Ladies, let me tell
                                you
                                about
                                myself -- I got a dick for a brain" being among the
                                calmer points. The album truly comes into its own with "When We Two Parted," though,
                                as
                                sad
                                countryish guitars chime over a slow crawling
                                rhythm and Dulli's quiet-then-anguished detailing of an exploding relationship. From
                                there
                                on in, things surge from strength to greater<br>
                                strength, sometimes due to the subtlest of touches -- the string arrangement on
                                "Fountain
                                and Fairfax" or the unexpected, resigned lead
                                vocal from Scrawl's Marcy Mays on "My Curse," for instance. Other times, it's all
                                the
                                much
                                more upfront, as "What Jail Is Like," with its<br>
                                heartbroken-and-fierce combination of piano, feedback, and drive building to an
                                explosive
                                chorus. Dulli's blend of utter abnegation and
                                masculine swagger may be a crutch, but when everything connects, as it does more
                                often
                                than
                                not on Gentlemen, both he and his band are
                                unstoppable.<br>
